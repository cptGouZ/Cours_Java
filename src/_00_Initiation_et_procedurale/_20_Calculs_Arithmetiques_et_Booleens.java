	public static void Maths() {
		/* La division de deux entier retourne la partie entière de la division quelque soit la variable de retour
		 * La division de deux float retourne un float
		 * La division de deux double retourne un double
		 * La division d'un entier par un float ou inversement donne un float
		 * La division d'un entier par un double ou inversement donne un double
		 * La division d'un float par un double ou inversement donne un double
		 * 
		 * Le reste d'une division entière est récupérable par le modulo '%'
		 */
		System.out.println( "Partie entière d'une division d'entier 5/3 :" + 5/3);
		System.out.println( "Modulo d'une division entière 5%3: " + 5%3 );
	}
